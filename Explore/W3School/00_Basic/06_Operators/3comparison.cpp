#include <iostream>

using namespace std;
int main()
{
  int x = 2, y = 3;
  /*
  1 = true 
  0 = false
  */

  cout << endl << "x < y = " << (x < y);
  cout << endl << "x > y = " << (x > y);
  cout << endl << "x != y = " << (x != y);
  cout << endl << "x == y = " << (x == y);
  cout << endl << "x <= y = " << (x <= y);
  cout << endl << "x >= y = " << (x >= y);

  return 0;
}